// add numbers
const Add = (a, b) => {
  return a + b;
}
// Dividing numbers
const Divide = (a, b) => {
  return a / b;
}
// Multiply the numbers
const Multiply = (a, b) => {
  return a * b;
}
// subtraction numbers
const Subtract = (a, b) => {
  return a - b;
}
export{Add, Divide, Multiply, Subtract}
